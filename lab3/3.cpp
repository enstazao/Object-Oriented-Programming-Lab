#include <iostream>
using namespace std;

int main()
{
    int c = 5;
    cout << "Behaviour of Post-increment" << c << endl;
    cout << c++ << endl;
    cout << c << endl;
    c = 5;
    cout << "Behaviour of Pre-Increment" << c << endl;
    cout << ++c << endl;
    cout << c << endl;
    return 0;

}